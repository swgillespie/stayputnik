use std::collections::BTreeMap;
use std::fmt::Write;
use std::path::Path;

use heck::{ToSnakeCase, ToUpperCamelCase};

use prost::Message;

// The codegen binary is deliberately self-contained: it cannot link against
// the library's client or generated services, because regenerating those is
// exactly what it is for — a codegen change that breaks the generated code
// would then also break the tool needed to fix it. So it decodes the schema
// (and speaks the wire protocol, for `capture`) on its own.
#[allow(dead_code)]
mod proto {
    include!(concat!(env!("OUT_DIR"), "/krpc.schema.rs"));
}

const DUMP_PATH: &str = "proto/services.bin";
const OUT_DIR: &str = "src/services";

#[tokio::main]
async fn main() -> Result<(), Box<dyn std::error::Error>> {
    let args: Vec<String> = std::env::args().skip(1).collect();
    match args.first().map(String::as_str) {
        Some("capture") => {
            let address = args.get(1).cloned().unwrap_or_else(|| "127.0.0.1".to_string());
            let port: u16 = args.get(2).and_then(|s| s.parse().ok()).unwrap_or(50000);
            capture(&address, port).await?;
        }
        Some("generate") | None => generate_all()?,
        Some(other) => {
            eprintln!("unknown command: {other}");
            eprintln!("usage: stayputnik-codegen capture [address] [port]");
            eprintln!("       stayputnik-codegen [generate]");
            std::process::exit(2);
        }
    }
    Ok(())
}

/// Connects to a running kRPC server and saves its `GetServices` response to
/// `proto/services.bin`, from which `generate` works offline.
async fn capture(address: &str, port: u16) -> Result<(), Box<dyn std::error::Error>> {
    eprintln!("Connecting to {address}:{port}...");
    let mut stream = tokio::net::TcpStream::connect((address, port)).await?;

    // Handshake
    let conn_req = proto::ConnectionRequest {
        r#type: proto::connection_request::Type::Rpc as i32,
        client_name: "stayputnik-codegen".to_string(),
        client_identifier: vec![],
    };
    send_message(&mut stream, &conn_req).await?;
    let resp_bytes = receive_message(&mut stream).await?;
    let conn_resp = proto::ConnectionResponse::decode(resp_bytes.as_slice())?;
    assert_eq!(conn_resp.status(), proto::connection_response::Status::Ok);
    eprintln!("Connected.");

    // Call GetServices
    let request = proto::Request {
        calls: vec![proto::ProcedureCall {
            service: "KRPC".to_string(),
            procedure: "GetServices".to_string(),
            ..Default::default()
        }],
    };
    send_message(&mut stream, &request).await?;
    let resp_bytes = receive_message(&mut stream).await?;
    let response = proto::Response::decode(resp_bytes.as_slice())?;
    let result = &response.results[0];

    // Validate before persisting.
    let services = proto::Services::decode(result.value.as_slice())?;
    std::fs::write(DUMP_PATH, &result.value)?;
    eprintln!(
        "Wrote {DUMP_PATH} ({} bytes, {} services)",
        result.value.len(),
        services.services.len()
    );
    Ok(())
}

fn generate_all() -> Result<(), Box<dyn std::error::Error>> {
    let bytes = std::fs::read(DUMP_PATH).map_err(|e| {
        format!("failed to read {DUMP_PATH}: {e}; run `stayputnik-codegen capture` against a running kRPC server first")
    })?;
    let services = proto::Services::decode(bytes.as_slice())?;

    eprintln!(
        "Loaded {} services from {DUMP_PATH}: {}",
        services.services.len(),
        services
            .services
            .iter()
            .map(|s| s.name.as_str())
            .collect::<Vec<_>>()
            .join(", ")
    );

    let out_dir = Path::new(OUT_DIR);

    // The output directory is wholly owned by the generator; clear stale
    // modules from previous runs (e.g. after a service is renamed).
    for entry in std::fs::read_dir(out_dir)? {
        let path = entry?.path();
        if path.extension().is_some_and(|ext| ext == "rs") {
            std::fs::remove_file(&path)?;
        }
    }

    let mut mod_lines = Vec::new();
    for service in &services.services {
        let module = module_name(&service.name);
        let code = generate_service(service);
        let path = out_dir.join(format!("{module}.rs"));
        std::fs::write(&path, &code)?;
        eprintln!("Generated {}", path.display());
        mod_lines.push(format!("pub mod {module};"));
    }

    mod_lines.sort();
    let mod_rs = format!("// Generated by stayputnik-codegen. Do not edit.\n\n{}\n", mod_lines.join("\n"));
    std::fs::write(out_dir.join("mod.rs"), &mod_rs)?;
    eprintln!("Generated {OUT_DIR}/mod.rs");

    Ok(())
}

// ---- Procedure name parsing ----

#[derive(Debug)]
enum ProcedureKind {
    ServiceProcedure,
    ServiceGetter { property: String },
    ServiceSetter { property: String },
    ClassMethod { class: String },
    ClassStaticMethod { class: String },
    ClassGetter { class: String, property: String },
    ClassSetter { class: String, property: String },
}

fn classify_procedure(proc_name: &str, class_names: &[&str]) -> ProcedureKind {
    // Try matching against known class names (longest first to avoid prefix ambiguity)
    for &class_name in class_names {
        let prefix = format!("{class_name}_");
        if let Some(rest) = proc_name.strip_prefix(&prefix) {
            if rest.strip_prefix("static_").is_some() {
                return ProcedureKind::ClassStaticMethod {
                    class: class_name.to_string(),
                };
            }
            if let Some(prop) = rest.strip_prefix("get_") {
                return ProcedureKind::ClassGetter {
                    class: class_name.to_string(),
                    property: prop.to_string(),
                };
            }
            if let Some(prop) = rest.strip_prefix("set_") {
                return ProcedureKind::ClassSetter {
                    class: class_name.to_string(),
                    property: prop.to_string(),
                };
            }
            return ProcedureKind::ClassMethod {
                class: class_name.to_string(),
            };
        }
    }

    // Service-level
    if let Some(prop) = proc_name.strip_prefix("get_") {
        return ProcedureKind::ServiceGetter {
            property: prop.to_string(),
        };
    }
    if let Some(prop) = proc_name.strip_prefix("set_") {
        return ProcedureKind::ServiceSetter {
            property: prop.to_string(),
        };
    }
    ProcedureKind::ServiceProcedure
}

// ---- Naming ----

/// Module name for a service. `heck` mangles mixed-case acronyms
/// (LiDAR -> li_dar), so special cases are listed explicitly.
fn module_name(service_name: &str) -> String {
    match service_name {
        "LiDAR" => "lidar".to_string(),
        _ => service_name.to_snake_case(),
    }
}

const RUST_KEYWORDS: &[&str] = &[
    "type", "move", "loop", "match", "ref", "self", "super", "use", "where", "while", "async",
    "await", "dyn", "abstract", "become", "box", "do", "final", "macro", "override", "priv",
    "typeof", "unsized", "virtual", "yield", "try", "mod", "in", "if", "else", "enum", "fn",
    "for", "impl", "let", "mut", "pub", "return", "static", "struct", "trait", "crate", "const",
    "continue", "break", "extern", "as",
];

fn safe_ident(name: &str) -> String {
    let snake = name.to_snake_case();
    if RUST_KEYWORDS.contains(&snake.as_str()) {
        format!("r#{snake}")
    } else {
        snake
    }
}

fn safe_setter_ident(prop_name: &str) -> String {
    let snake = prop_name.to_snake_case();
    // Can't do set_r#type — use set_type_ instead for keyword properties
    if RUST_KEYWORDS.contains(&snake.as_str()) {
        format!("set_{snake}_")
    } else {
        format!("set_{snake}")
    }
}

// ---- Documentation ----
//
// kRPC service documentation is C#-style XML (<doc><summary>..</summary>
// <param name=".."/>..</doc>). These helpers convert it, best-effort, into
// rustdoc markdown.

fn unescape_entities(s: &str) -> String {
    s.replace("&lt;", "<")
        .replace("&gt;", ">")
        .replace("&quot;", "\"")
        .replace("&apos;", "'")
        .replace("&amp;", "&")
}

/// Extracts the value of `key="..."` from a raw tag body.
fn tag_attr(tag: &str, key: &str) -> Option<String> {
    let pat = format!("{key}=\"");
    let start = tag.find(&pat)? + pat.len();
    let end = tag[start..].find('"')? + start;
    Some(tag[start..end].to_string())
}

/// `M:SpaceCenter.Vessel.Position` -> `SpaceCenter.Vessel.Position`
fn clean_cref(cref: &str) -> &str {
    match cref.split_once(':') {
        Some((kind, rest)) if kind.len() == 1 => rest,
        _ => cref,
    }
}

fn collapse_ws(s: &str) -> String {
    s.split_whitespace().collect::<Vec<_>>().join(" ")
}

#[derive(Default)]
struct DocSections {
    summary: String,
    remarks: String,
    returns: String,
    params: Vec<(String, String)>,
}

/// Converts a kRPC XML doc string into rustdoc markdown lines (without the
/// `///` prefix). Returns an empty vec when there is nothing to say.
fn doc_lines(xml: &str) -> Vec<String> {
    #[derive(PartialEq, Clone, Copy)]
    enum Section {
        None,
        Summary,
        Remarks,
        Returns,
        Param,
    }

    let mut sections = DocSections::default();
    let mut section = Section::None;
    // Inline tags (<c>, <math>, <a>) buffer their content and transform it
    // when the tag closes. The String is the buffered content; the first
    // field is the tag body it was opened with.
    let mut captures: Vec<(String, String)> = Vec::new();

    // Appends converted text to whatever is currently open.
    fn emit(
        text: &str,
        section: Section,
        sections: &mut DocSections,
        captures: &mut [(String, String)],
    ) {
        let buf = if let Some((_, buf)) = captures.last_mut() {
            buf
        } else {
            match section {
                Section::Summary => &mut sections.summary,
                Section::Remarks => &mut sections.remarks,
                Section::Returns => &mut sections.returns,
                Section::Param => match sections.params.last_mut() {
                    Some((_, text)) => text,
                    None => return,
                },
                Section::None => return,
            }
        };
        buf.push_str(text);
    }

    let mut rest = xml.trim();
    while !rest.is_empty() {
        let Some(lt) = rest.find('<') else {
            let text = unescape_entities(rest).replace('\n', " ");
            emit(&text, section, &mut sections, &mut captures);
            break;
        };
        if lt > 0 {
            let text = unescape_entities(&rest[..lt]).replace('\n', " ");
            emit(&text, section, &mut sections, &mut captures);
        }
        let Some(gt) = rest[lt..].find('>').map(|i| i + lt) else {
            break; // malformed; drop the remainder
        };
        let tag = rest[lt + 1..gt].trim_end_matches('/').trim();
        let is_close = tag.starts_with('/');
        let self_closing = rest[lt + 1..gt].trim_end().ends_with('/');
        let name = tag
            .trim_start_matches('/')
            .split_whitespace()
            .next()
            .unwrap_or("");

        match (name, is_close) {
            ("doc" | "description", _) => {}
            ("summary", false) => section = Section::Summary,
            ("remarks", false) => section = Section::Remarks,
            ("returns", false) => section = Section::Returns,
            ("param", false) => {
                let pname = tag_attr(tag, "name").unwrap_or_default();
                sections.params.push((safe_ident(&pname), String::new()));
                section = Section::Param;
            }
            ("summary" | "remarks" | "returns" | "param", true) => section = Section::None,
            ("see", false) => {
                let cref = tag_attr(tag, "cref").unwrap_or_default();
                let text = format!("`{}`", clean_cref(&cref));
                emit(&text, section, &mut sections, &mut captures);
            }
            ("paramref", false) => {
                let pname = tag_attr(tag, "name").unwrap_or_default();
                let text = format!("`{}`", safe_ident(&pname));
                emit(&text, section, &mut sections, &mut captures);
            }
            ("c" | "math" | "a", false) if !self_closing => {
                captures.push((tag.to_string(), String::new()));
            }
            ("c", true) => {
                if let Some((_, buf)) = captures.pop() {
                    let inner = buf.trim();
                    // kRPC's `null` is `None` on this side of the wire.
                    let text = if inner == "null" {
                        "`None`".to_string()
                    } else {
                        format!("`{inner}`")
                    };
                    emit(&text, section, &mut sections, &mut captures);
                }
            }
            ("math", true) => {
                if let Some((_, buf)) = captures.pop() {
                    let text = format!("`{}`", buf.trim());
                    emit(&text, section, &mut sections, &mut captures);
                }
            }
            ("a", true) => {
                if let Some((open_tag, buf)) = captures.pop() {
                    let href = tag_attr(&open_tag, "href").unwrap_or_default();
                    let text = format!("[{}]({href})", buf.trim());
                    emit(&text, section, &mut sections, &mut captures);
                }
            }
            ("list", _) => emit("\n", section, &mut sections, &mut captures),
            ("item", false) => emit("\n- ", section, &mut sections, &mut captures),
            ("item", true) => {}
            _ => {} // unknown tag: drop the markup, keep surrounding text
        }

        rest = &rest[gt + 1..];
    }

    // Assemble markdown.
    fn push_block(out: &mut Vec<String>, text: &str) {
        for line in text.split('\n') {
            let line = collapse_ws(line);
            if !line.is_empty() {
                out.push(line);
            }
        }
    }
    fn push_separated(out: &mut Vec<String>, text: &str) {
        if !collapse_ws(text).is_empty() {
            if !out.is_empty() {
                out.push(String::new());
            }
            push_block(out, text);
        }
    }

    let mut md = Vec::new();
    push_block(&mut md, &sections.summary);
    push_separated(&mut md, &sections.remarks);

    let documented: Vec<_> = sections
        .params
        .iter()
        .filter(|(_, text)| !collapse_ws(text).is_empty())
        .collect();
    if !documented.is_empty() {
        if !md.is_empty() {
            md.push(String::new());
        }
        md.push("# Arguments".to_string());
        md.push(String::new());
        for (pname, text) in documented {
            md.push(format!("* `{pname}` - {}", collapse_ws(text)));
        }
    }

    if !collapse_ws(&sections.returns).is_empty() {
        if !md.is_empty() {
            md.push(String::new());
        }
        md.push("# Returns".to_string());
        md.push(String::new());
        push_block(&mut md, &sections.returns);
    }

    md
}

fn write_doc(out: &mut String, indent: &str, xml: &str) {
    for line in doc_lines(xml) {
        if line.is_empty() {
            writeln!(out, "{indent}///").unwrap();
        } else {
            writeln!(out, "{indent}/// {line}").unwrap();
        }
    }
}

// ---- Type mapping ----

fn rust_type(ty: &proto::Type, current_service: &str) -> String {
    use proto::r#type::TypeCode;
    match ty.code() {
        TypeCode::None => "()".into(),
        TypeCode::Double => "f64".into(),
        TypeCode::Float => "f32".into(),
        TypeCode::Sint32 => "i32".into(),
        TypeCode::Sint64 => "i64".into(),
        TypeCode::Uint32 => "u32".into(),
        TypeCode::Uint64 => "u64".into(),
        TypeCode::Bool => "bool".into(),
        TypeCode::String => "String".into(),
        TypeCode::Bytes => "Vec<u8>".into(),
        TypeCode::Class | TypeCode::Enumeration => {
            if ty.service == current_service {
                ty.name.clone()
            } else {
                format!("super::{}::{}", module_name(&ty.service), ty.name)
            }
        }
        // Protocol-level message types
        TypeCode::Event => "crate::krpc::schema::Event".into(),
        TypeCode::ProcedureCall => "crate::krpc::schema::ProcedureCall".into(),
        TypeCode::Stream => "crate::krpc::schema::Stream".into(),
        TypeCode::Status => "crate::krpc::schema::Status".into(),
        TypeCode::Services => "crate::krpc::schema::Services".into(),
        TypeCode::List => {
            let inner = rust_type(&ty.types[0], current_service);
            format!("Vec<{inner}>")
        }
        TypeCode::Dictionary => {
            let key = rust_type(&ty.types[0], current_service);
            let val = rust_type(&ty.types[1], current_service);
            format!("std::collections::HashMap<{key}, {val}>")
        }
        TypeCode::Set => {
            let inner = rust_type(&ty.types[0], current_service);
            format!("std::collections::HashSet<{inner}>")
        }
        TypeCode::Tuple => {
            let inners: Vec<_> = ty.types.iter().map(|t| rust_type(t, current_service)).collect();
            format!("({})", inners.join(", "))
        }
    }
}

fn is_class_type(ty: &proto::Type) -> bool {
    ty.code() == proto::r#type::TypeCode::Class
}

/// Parameter type: borrow anything that isn't trivially copyable.
fn param_type_str(ty: &proto::Type, current_service: &str) -> String {
    use proto::r#type::TypeCode;
    match ty.code() {
        TypeCode::String => "&str".into(),
        TypeCode::Bytes => "&[u8]".into(),
        TypeCode::Class
        | TypeCode::Event
        | TypeCode::ProcedureCall
        | TypeCode::Stream
        | TypeCode::Status
        | TypeCode::Services => format!("&{}", rust_type(ty, current_service)),
        _ => rust_type(ty, current_service),
    }
}

fn return_type_str(proc: &proto::Procedure, service_name: &str) -> String {
    match &proc.return_type {
        Some(ty) if ty.code() != proto::r#type::TypeCode::None => {
            let rt = rust_type(ty, service_name);
            if proc.return_is_nullable && is_class_type(ty) {
                format!("Option<{rt}>")
            } else {
                rt
            }
        }
        _ => "()".into(),
    }
}

fn returns_value(proc: &proto::Procedure) -> bool {
    matches!(&proc.return_type, Some(ty) if ty.code() != proto::r#type::TypeCode::None)
}

// ---- Code generation ----

struct ClassDef {
    name: String,
    documentation: String,
    methods: Vec<proto::Procedure>,
    static_methods: Vec<proto::Procedure>,
    getters: BTreeMap<String, proto::Procedure>,
    setters: BTreeMap<String, proto::Procedure>,
}

struct ServiceDef {
    name: String,
    documentation: String,
    procedures: Vec<proto::Procedure>,
    getters: BTreeMap<String, proto::Procedure>,
    setters: BTreeMap<String, proto::Procedure>,
    classes: BTreeMap<String, ClassDef>,
    enums: Vec<proto::Enumeration>,
}

fn generate_service(service: &proto::Service) -> String {
    let mut class_names: Vec<&str> = service.classes.iter().map(|c| c.name.as_str()).collect();
    // Sort longest first to handle prefix ambiguity
    class_names.sort_by_key(|name| std::cmp::Reverse(name.len()));

    let mut svc = ServiceDef {
        name: service.name.clone(),
        documentation: service.documentation.clone(),
        procedures: Vec::new(),
        getters: BTreeMap::new(),
        setters: BTreeMap::new(),
        classes: BTreeMap::new(),
        enums: service.enumerations.clone(),
    };

    // Initialize class defs
    for class in &service.classes {
        svc.classes.insert(
            class.name.clone(),
            ClassDef {
                name: class.name.clone(),
                documentation: class.documentation.clone(),
                methods: Vec::new(),
                static_methods: Vec::new(),
                getters: BTreeMap::new(),
                setters: BTreeMap::new(),
            },
        );
    }

    // Classify each procedure
    for proc in &service.procedures {
        match classify_procedure(&proc.name, &class_names) {
            ProcedureKind::ServiceProcedure => svc.procedures.push(proc.clone()),
            ProcedureKind::ServiceGetter { property } => {
                svc.getters.insert(property, proc.clone());
            }
            ProcedureKind::ServiceSetter { property } => {
                svc.setters.insert(property, proc.clone());
            }
            ProcedureKind::ClassMethod { class } => {
                if let Some(cd) = svc.classes.get_mut(&class) {
                    cd.methods.push(proc.clone());
                }
            }
            ProcedureKind::ClassStaticMethod { class } => {
                if let Some(cd) = svc.classes.get_mut(&class) {
                    cd.static_methods.push(proc.clone());
                }
            }
            ProcedureKind::ClassGetter { class, property } => {
                if let Some(cd) = svc.classes.get_mut(&class) {
                    cd.getters.insert(property, proc.clone());
                }
            }
            ProcedureKind::ClassSetter { class, property } => {
                if let Some(cd) = svc.classes.get_mut(&class) {
                    cd.setters.insert(property, proc.clone());
                }
            }
        }
    }

    let mut body = String::new();

    // Generate enums
    for e in &svc.enums {
        write_enum(&mut body, e);
    }

    // Generate class structs
    for class in svc.classes.values() {
        write_class(&mut body, class, &svc.name);
    }

    // Generate service struct
    write_service_struct(&mut body, &svc);

    // Header: only import what the body actually uses.
    let mut out = String::new();
    writeln!(out, "// Generated by stayputnik-codegen. Do not edit.").unwrap();
    writeln!(out).unwrap();
    // No file-level lint allows: clippy::mutable_key_type is handled by
    // clippy.toml (ignore-interior-mutability for ClientRef), and
    // clippy::too_many_arguments is allowed per-method where it fires.
    // `codec::arg` is referenced via the module path rather than imported
    // directly: kRPC procedures have parameters named `arg`, which would
    // shadow a bare `arg` binding at the call site.
    let mut imports: Vec<&str> = Vec::new();
    if body.contains("codec::arg(") {
        imports.push("self");
    }
    if body.contains("Decode") {
        imports.push("Decode");
    }
    if body.contains("Encode") {
        imports.push("Encode");
    }
    if !imports.is_empty() {
        writeln!(out, "use crate::codec::{{{}}};", imports.join(", ")).unwrap();
        writeln!(out).unwrap();
    }
    out.push_str(&body);
    out
}

fn write_enum(out: &mut String, e: &proto::Enumeration) {
    write_doc(out, "", &e.documentation);
    writeln!(out, "#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]").unwrap();
    writeln!(out, "#[repr(i32)]").unwrap();
    writeln!(out, "pub enum {} {{", e.name).unwrap();
    for val in &e.values {
        write_doc(out, "    ", &val.documentation);
        writeln!(out, "    {} = {},", val.name.to_upper_camel_case(), val.value).unwrap();
    }
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    // TryFrom<i32>
    writeln!(out, "impl TryFrom<i32> for {} {{", e.name).unwrap();
    writeln!(out, "    type Error = crate::Error;").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "    fn try_from(value: i32) -> Result<Self, Self::Error> {{").unwrap();
    writeln!(out, "        match value {{").unwrap();
    for val in &e.values {
        writeln!(
            out,
            "            {} => Ok(Self::{}),",
            val.value,
            val.name.to_upper_camel_case()
        )
        .unwrap();
    }
    writeln!(
        out,
        "            _ => Err(crate::Error::Decode(prost::DecodeError::new(format!(\"unknown enum value {{}} for {}\", value)))),",
        e.name
    )
    .unwrap();
    writeln!(out, "        }}").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    // Encode
    writeln!(out, "impl Encode for {} {{", e.name).unwrap();
    writeln!(out, "    fn encode_krpc(&self) -> Vec<u8> {{").unwrap();
    writeln!(out, "        (*self as i32).encode_krpc()").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    // Decode
    writeln!(out, "impl Decode for {} {{", e.name).unwrap();
    writeln!(out, "    fn decode_krpc(client: &crate::ClientRef, bytes: &[u8]) -> crate::Result<Self> {{").unwrap();
    writeln!(out, "        Self::try_from(<i32 as Decode>::decode_krpc(client, bytes)?)").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();
}

fn write_class(out: &mut String, class: &ClassDef, service_name: &str) {
    let name = &class.name;

    write_doc(out, "", &class.documentation);
    if !doc_lines(&class.documentation).is_empty() {
        writeln!(out, "///").unwrap();
    }
    writeln!(out, "/// A remote object handle for a `{service_name}.{name}` instance.").unwrap();
    writeln!(out, "#[derive(Debug, Clone)]").unwrap();
    writeln!(out, "pub struct {name} {{").unwrap();
    // Classes with no instance procedures never read the client; it is kept
    // anyway so all handles decode and behave uniformly.
    let has_instance_procs =
        !(class.getters.is_empty() && class.setters.is_empty() && class.methods.is_empty());
    if has_instance_procs {
        writeln!(out, "    client: crate::ClientRef,").unwrap();
    } else {
        writeln!(out, "    #[allow(dead_code)]").unwrap();
        writeln!(out, "    client: crate::ClientRef,").unwrap();
    }
    writeln!(out, "    pub(crate) id: u64,").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "impl {name} {{").unwrap();
    writeln!(out, "    pub(crate) fn new(client: crate::ClientRef, id: u64) -> Self {{").unwrap();
    writeln!(out, "        Self {{ client, id }}").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "    /// The id of the remote object this handle refers to.").unwrap();
    writeln!(out, "    pub fn object_id(&self) -> u64 {{").unwrap();
    writeln!(out, "        self.id").unwrap();
    writeln!(out, "    }}").unwrap();

    // Property getters
    for (prop_name, proc) in &class.getters {
        write_method(out, service_name, proc, &safe_ident(prop_name), Receiver::Instance);
    }

    // Property setters
    for (prop_name, proc) in &class.setters {
        write_method(out, service_name, proc, &safe_setter_ident(prop_name), Receiver::Instance);
    }

    // Instance methods
    for proc in &class.methods {
        let method_name_raw = proc
            .name
            .split_once('_')
            .map(|(_, rest)| rest)
            .unwrap_or(&proc.name);
        write_method(out, service_name, proc, &safe_ident(method_name_raw), Receiver::Instance);
    }

    // Static methods
    for proc in &class.static_methods {
        let method_name_raw = proc.name.split("_static_").nth(1).unwrap_or(&proc.name);
        write_method(out, service_name, proc, &safe_ident(method_name_raw), Receiver::Static);
    }

    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    // Handle equality is identity of the remote object.
    writeln!(out, "impl PartialEq for {name} {{").unwrap();
    writeln!(out, "    fn eq(&self, other: &Self) -> bool {{").unwrap();
    writeln!(out, "        self.id == other.id").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "impl Eq for {name} {{}}").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "impl std::hash::Hash for {name} {{").unwrap();
    writeln!(out, "    fn hash<H: std::hash::Hasher>(&self, state: &mut H) {{").unwrap();
    writeln!(out, "        self.id.hash(state);").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    // Encode/Decode by remote object id
    writeln!(out, "impl Encode for {name} {{").unwrap();
    writeln!(out, "    fn encode_krpc(&self) -> Vec<u8> {{").unwrap();
    writeln!(out, "        self.id.encode_krpc()").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "impl Decode for {name} {{").unwrap();
    writeln!(out, "    fn decode_krpc(client: &crate::ClientRef, bytes: &[u8]) -> crate::Result<Self> {{").unwrap();
    writeln!(out, "        let id = <u64 as Decode>::decode_krpc(client, bytes)?;").unwrap();
    writeln!(out, "        Ok(Self::new(client.clone(), id))").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    // Nullable handles decode id 0 as None.
    writeln!(out, "impl Decode for Option<{name}> {{").unwrap();
    writeln!(out, "    fn decode_krpc(client: &crate::ClientRef, bytes: &[u8]) -> crate::Result<Self> {{").unwrap();
    writeln!(out, "        let id = <u64 as Decode>::decode_krpc(client, bytes)?;").unwrap();
    writeln!(out, "        Ok(if id == 0 {{ None }} else {{ Some({name}::new(client.clone(), id)) }})").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();
}

fn write_service_struct(out: &mut String, svc: &ServiceDef) {
    let struct_name = &svc.name;
    if doc_lines(&svc.documentation).is_empty() {
        writeln!(out, "/// The `{struct_name}` service.").unwrap();
    } else {
        write_doc(out, "", &svc.documentation);
    }
    writeln!(out, "pub struct {struct_name} {{").unwrap();
    writeln!(out, "    client: crate::ClientRef,").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();

    writeln!(out, "impl {struct_name} {{").unwrap();
    writeln!(out, "    pub fn new(client: crate::ClientRef) -> Self {{").unwrap();
    writeln!(out, "        Self {{ client }}").unwrap();
    writeln!(out, "    }}").unwrap();

    for (prop_name, proc) in &svc.getters {
        write_method(out, &svc.name, proc, &safe_ident(prop_name), Receiver::Service);
    }

    for (prop_name, proc) in &svc.setters {
        write_method(out, &svc.name, proc, &safe_setter_ident(prop_name), Receiver::Service);
    }

    for proc in &svc.procedures {
        write_method(out, &svc.name, proc, &safe_ident(&proc.name), Receiver::Service);
    }

    writeln!(out, "}}").unwrap();
}

#[derive(Clone, Copy, PartialEq)]
enum Receiver {
    /// Class instance method: `&self`, implicit `codec::arg(0, &self.id)`, user params from position 1.
    Instance,
    /// Class static method: explicit `client: &crate::ClientRef` parameter.
    Static,
    /// Service-level method: `&self`, no implicit id argument.
    Service,
}

/// Emits one generated method. All procedure shapes (getters, setters,
/// methods, static methods, service procedures) share this single form.
fn write_method(
    out: &mut String,
    service_name: &str,
    proc: &proto::Procedure,
    method_name: &str,
    receiver: Receiver,
) {
    let ret_type = return_type_str(proc, service_name);

    // For instance receivers the first proto parameter is the implicit `this`.
    let user_params: Vec<&proto::Parameter> = match receiver {
        Receiver::Instance => proc.parameters.iter().skip(1).collect(),
        Receiver::Static | Receiver::Service => proc.parameters.iter().collect(),
    };
    let position_offset: usize = if receiver == Receiver::Instance { 1 } else { 0 };

    let mut sig_params = String::new();
    for p in &user_params {
        let name = safe_ident(&p.name);
        let ty = p.r#type.as_ref().unwrap();
        let type_str = if !p.default_value.is_empty() {
            format!("Option<{}>", rust_type(ty, service_name))
        } else {
            param_type_str(ty, service_name)
        };
        write!(sig_params, ", {name}: {type_str}").unwrap();
    }

    let recv = match receiver {
        Receiver::Instance | Receiver::Service => "&self",
        Receiver::Static => "client: &crate::ClientRef",
    };
    let client_expr = match receiver {
        Receiver::Instance | Receiver::Service => "self.client",
        Receiver::Static => "client",
    };
    // `client` in static methods is already a `&ClientRef`.
    let client_ref_expr = match receiver {
        Receiver::Instance | Receiver::Service => "&self.client",
        Receiver::Static => "client",
    };

    let too_many_args = user_params.len() + 1 > 7;

    // The plain RPC method.
    writeln!(out).unwrap();
    write_doc(out, "    ", &proc.documentation);
    // The receiver (or `client` parameter) counts toward clippy's
    // too-many-arguments threshold of 7.
    if too_many_args {
        writeln!(out, "    #[allow(clippy::too_many_arguments)]").unwrap();
    }
    writeln!(out, "    pub async fn {method_name}({recv}{sig_params}) -> crate::Result<{ret_type}> {{").unwrap();
    let args_expr = write_args(out, &user_params, receiver, position_offset);
    if returns_value(proc) {
        writeln!(
            out,
            "        let data = {client_expr}.invoke(\"{service_name}\", \"{}\", {args_expr}).await?;",
            proc.name
        )
        .unwrap();
        writeln!(out, "        Decode::decode_krpc({client_ref_expr}, &data)").unwrap();
    } else {
        writeln!(
            out,
            "        {client_expr}.invoke(\"{service_name}\", \"{}\", {args_expr}).await?;",
            proc.name
        )
        .unwrap();
        writeln!(out, "        Ok(())").unwrap();
    }
    writeln!(out, "    }}").unwrap();

    // The streamed variant: the server executes the procedure repeatedly
    // and pushes results. Only value-returning procedures are streamable.
    if returns_value(proc) {
        writeln!(out).unwrap();
        writeln!(out, "    /// Streamed variant of `{method_name}`: the server pushes the value").unwrap();
        writeln!(out, "    /// at the stream's update rate instead of being polled.").unwrap();
        if too_many_args {
            writeln!(out, "    #[allow(clippy::too_many_arguments)]").unwrap();
        }
        writeln!(out, "    pub async fn {method_name}_stream({recv}{sig_params}) -> crate::Result<crate::Stream<{ret_type}>> {{").unwrap();
        let args_expr = write_args(out, &user_params, receiver, position_offset);
        writeln!(
            out,
            "        {client_expr}.stream(\"{service_name}\", \"{}\", {args_expr}).await",
            proc.name
        )
        .unwrap();
        writeln!(out, "    }}").unwrap();
    }
}

/// Emits the argument-building statements for a method body and returns the
/// expression naming the arguments.
fn write_args(
    out: &mut String,
    user_params: &[&proto::Parameter],
    receiver: Receiver,
    position_offset: usize,
) -> String {
    // Required arguments, up to the first optional one, are emitted inline;
    // any optional arguments require a mutable vec.
    let mut inline_args: Vec<String> = Vec::new();
    if receiver == Receiver::Instance {
        inline_args.push("codec::arg(0, &self.id)".to_string());
    }
    let first_optional = user_params
        .iter()
        .position(|p| !p.default_value.is_empty())
        .unwrap_or(user_params.len());
    for (i, p) in user_params.iter().take(first_optional).enumerate() {
        let name = safe_ident(&p.name);
        inline_args.push(format!("codec::arg({}, &{name})", i + position_offset));
    }

    if first_optional == user_params.len() {
        // All required: borrow an array directly.
        format!("&[{}]", inline_args.join(", "))
    } else {
        writeln!(out, "        let mut args = vec![{}];", inline_args.join(", ")).unwrap();
        for (i, p) in user_params.iter().enumerate().skip(first_optional) {
            let name = safe_ident(&p.name);
            let pos = i + position_offset;
            if !p.default_value.is_empty() {
                writeln!(out, "        if let Some(v) = &{name} {{").unwrap();
                writeln!(out, "            args.push(codec::arg({pos}, v));").unwrap();
                writeln!(out, "        }}").unwrap();
            } else {
                writeln!(out, "        args.push(codec::arg({pos}, &{name}));").unwrap();
            }
        }
        "&args".to_string()
    }
}

// ---- Wire protocol helpers (self-contained; see note on `mod proto`) ----

async fn send_message(
    stream: &mut tokio::net::TcpStream,
    msg: &impl prost::Message,
) -> Result<(), Box<dyn std::error::Error>> {
    use tokio::io::AsyncWriteExt;
    let msg_bytes = msg.encode_to_vec();
    let mut buf = Vec::new();
    prost::encoding::encode_varint(msg_bytes.len() as u64, &mut buf);
    buf.extend_from_slice(&msg_bytes);
    stream.write_all(&buf).await?;
    Ok(())
}

async fn receive_message(
    stream: &mut tokio::net::TcpStream,
) -> Result<Vec<u8>, Box<dyn std::error::Error>> {
    use tokio::io::AsyncReadExt;
    let mut varint_buf = Vec::new();
    loop {
        let byte = stream.read_u8().await?;
        varint_buf.push(byte);
        if byte & 0x80 == 0 {
            break;
        }
    }
    let size = prost::encoding::decode_varint(&mut varint_buf.as_slice())? as usize;
    let mut msg_buf = vec![0u8; size];
    stream.read_exact(&mut msg_buf).await?;
    Ok(msg_buf)
}
